    //checks whether the form is correctly filled or not
    public boolean formisempty(){
        String m=(combosender.getSelectedItem()).toString();
        String n=(comborec.getSelectedItem()).toString();

        if(tweight.getText().isEmpty()==true)
            return true;
        else if(torderdate.getText().isEmpty()==true)
            return true;
        else if(tdestination.getText().isEmpty()==true)
            return true;
        else if (tsender.getText().isEmpty()==true)
            return true;
        else if(m.compareTo("SELECT ONE")==0)
            return true;
        else if(n.compareTo("SELECT ONE")==0)
            return true;
        else if(m.compareTo(n)==0)
            return true;

        if(radiosmall.isSelected()==false){
            if(tvolume.getText().isEmpty()==true||tdistance.getText().isEmpty()==true)
                 return true;
        }
            return false;
     }
