void clearall(){
    tweight.setText("");
    tvolume.setText("");
    torderdate.setText("");
    tdestination.setText("");
    tsender.setText("");
    combosender.setSelectedIndex(0);
    comborec.setSelectedIndex(0);
    tdistance.setText("");
    return;
    
}

    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        int newid=0,price=0;
        if(formisempty()==true){
      JOptionPane.showMessageDialog(this,"Please fill the form fully and correctly");
      return;
        }

        consignment neworder= new consignment(this);

        try {
            neworder.inc_con_num();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(orderform.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(orderform.class.getName()).log(Level.SEVERE, null, ex);
        }
        price=neworder.price();
       JOptionPane.showMessageDialog(this, "Your Consignment ID is " + Integer.toString(neworder.ID) + "\nPrice is " + Integer.toString(price) + "\nPlease remember your consignment ID. \n You will require it in future for viewing your Consignment status \n Your receipt is being printed");

       try {
            neworder.update_consignment_database();
        } catch (IOException ex) {
            Logger.getLogger(orderform.class.getName()).log(Level.SEVERE, null, ex);
        }
       branch branch1=new branch();
       account account1=new account();
       branch1.name=combosender.getSelectedItem().toString();
        try {
            branch1.update_rev_gen(price);
            branch1.update_vol((Double.parseDouble(tvolume.getText())));
            account1.update_rev_gen(price);
        } catch (IOException ex) {
            Logger.getLogger(orderform.class.getName()).log(Level.SEVERE, null, ex);
        }

       clearall();

    }//GEN-LAST:event_confirmActionPerformed

